typedef __int128_t int128_t;
typedef __uint128_t uint128_t;

typedef union {
	uint32_t ui32;
	int32_t i32;
	float f;
} binary32;

static inline int64_t toFixedPoint(const float f) {
	binary32 b;
	b.f = f;
	int32_t e = ((b.ui32 << 1) >> 24) - 127;
	int64_t mantisse = ((b.ui32 << 9) >> 9);
	int sign = b.ui32 >> 31;
	int64_t val = (mantisse << 32) + ((int64_t)0b1 << 55);
	
	if (e > 0) {
		val = val << e;
	} else {
		val = val >> (-e);
	}

	if (sign > 0) {
		val = -val;
	}
	return val;
}

static inline int64_t mult(int64_t a, int64_t b) {
	int128_t m = (int128_t)a * (int128_t)b;
	return (int64_t)(m >> 55);
}

static inline int64_t round(int64_t x) { //round to the nearest integer
	int64_t val = (x >> 55) << 55;
	int64_t up = x & ((int64_t)0b1 << 54);
	if (up) {
		val += ((int64_t)0b1 << 55);
	}
	return val;
}

